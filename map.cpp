// 현재 스테이지의 맵을 화면에 출력하는 함수
// level: 출력할 스테이지 인덱스 (0 ~ STAGE_COUNT-1)
void drawMap(int level){
    // 스테이지 범위 체크
    if (level < 0 || level >= STAGE_COUNT) level = 0;
    
    for(int y=0; y<MAP_SIZE; y++){
        for(int x=0; x<MAP_SIZE; x++){

            // 현재 스테이지의 해당 칸 정보 가져오기
            int tile = maps[level][y][x];
            
            switch (tile){
                case 2: // 부술 수 없는 벽
                    attron(COLOR_PAIR(2));
                    mvprintw(y, x*2, "  "); // x*2를 하는 이유는 터미널 문자가 세로로 길기 때문에 정사각형을 맞추기 위함
                    attroff(COLOR_PAIR(2));
                    break;
                case 1: // 일반 벽
                    attron(COLOR_PAIR(1));
                    mvprintw(y, x*2, "  ");
                    attroff(COLOR_PAIR(1));
                    break;
                case 3: // 뱀 머리
                    attron(COLOR_PAIR(3));
                    mvprintw(y, x*2, "  ");
                    attroff(COLOR_PAIR(3));
                   break;
                case 4: // 뱀 몸통
                    attron(COLOR_PAIR(4));
                    mvprintw(y, x*2, "  ");
                    attroff(COLOR_PAIR(4));
                   break;
                case 0: // 빈 공간
                    mvprintw(y, x*2, "  ");
                    break;
            }
        }
    }
}
