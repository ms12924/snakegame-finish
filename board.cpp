void Board::reset(int stage) {
    for (int y = 0; y < MAP_SIZE; y++)
        for (int x = 0; x < MAP_SIZE; x++)
            map[stage][y][x] = originalMap[stage][y][x];
}

int Board::getCell(int stage, int y, int x) const {
    return map[stage][y][x];
}

void Board::setCell(int stage, int y, int x, int val) {
    map[stage][y][x] = val;
}

void Board::draw(int stage, bool invincible) const {
    for (int y = 0; y < MAP_SIZE; y++) {
        for (int x = 0; x < MAP_SIZE; x++) {
            int tile = map[stage][y][x];
            switch (tile) {
                case 2:
                    attron(COLOR_PAIR(2));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(2));
                    break;
                case 1:
                    attron(COLOR_PAIR(1));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(1));
                    break;
                case 3: {
                    int pair = invincible ? 8 : 3;
                    attron(COLOR_PAIR(pair));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(pair));
                    break;
                }
                case 4:
                    attron(COLOR_PAIR(4));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(4));
                    break;
                case 5:
                    attron(COLOR_PAIR(5));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(5));
                    break;
                case 6:
                    attron(COLOR_PAIR(6));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(6));
                    break;
                case 7:
                    attron(COLOR_PAIR(7));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(7));
                    break;
                case 8:
                    attron(COLOR_PAIR(8));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(8));
                    break;
                case 0:
                    mvprintw(y, x * 2, "  ");
                    break;
            }
        }
    }
}
