// ----------------------------------------------------------------------------
// reset: 저장된 백업 데이터를 사용해 특정 스테이지 게임판 초기화
// ----------------------------------------------------------------------------
void Board::reset(int stage) {
    for (int y = 0; y < MAP_SIZE; y++) {
        for (int x = 0; x < MAP_SIZE; x++) {
            map[stage][y][x] = originalMap[stage][y][x];
        }
    }
}

// 간단한 타일 정보 반환 조회 (상수 함수)
int Board::getCell(int stage, int y, int x) const {
    return map[stage][y][x];
}

// 간단한 타일 값 수정 세팅
void Board::setCell(int stage, int y, int x, int val) {
    map[stage][y][x] = val;
}

// ----------------------------------------------------------------------------
// draw: ncurses를 이용해 타일 상태에 대응하는 색상 레이아웃을 화면에 출력
// ----------------------------------------------------------------------------
void Board::draw(int stage, bool invincible) const {
    for (int y = 0; y < MAP_SIZE; y++) {
        for (int x = 0; x < MAP_SIZE; x++) {
            int tile = map[stage][y][x];
            
            // ncurses 터미널 특성상 가로 글자 폭이 좁으므로, 정사각형 배치를 위해 x 좌표에 2를 곱해 "  " 공백 단위로 렌더링함
            switch (tile) {
                case 2: // 모서리 Immune 벽
                    attron(COLOR_PAIR(2));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(2));
                    break;
                case 1: // 일반 벽
                    attron(COLOR_PAIR(1));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(1));
                    break;
                case 3: { // 뱀 머리 (무적 아이템 지속 여부에 따라 색상 다르게 스위칭)
                    int pair = invincible ? 8 : 3;
                    attron(COLOR_PAIR(pair));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(pair));
                    break;
                }
                case 4: // 뱀 몸통
                    attron(COLOR_PAIR(4));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(4));
                    break;
                case 5: // 성장 먹이 아이템
                    attron(COLOR_PAIR(5));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(5));
                    break;
                case 6: // 감소 독 아이템
                    attron(COLOR_PAIR(6));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(6));
                    break;
                case 7: // 순간이동 게이트
                    attron(COLOR_PAIR(7));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(7));
                    break;
                case 8: // 특수 속성 타일 1
                    attron(COLOR_PAIR(8));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(8));
                    break;
                case 9: // 특수 속성 타일 2
                    attron(COLOR_PAIR(9));
                    mvprintw(y, x * 2, "  ");
                    attroff(COLOR_PAIR(9));
                    break;
                case 0: // 빈 공간
                    mvprintw(y, x * 2, "  ");
                    break;
            }
        }
    }
}
